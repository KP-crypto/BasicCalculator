class Calculator:
    def add_two_numbers(self,n1,n2):
        sum=n1+n2
        return sum

    def sub_two_numbers(self,n1,n2):
        sub=n1-n2
        return sub

    def multiply(self,n1,n2):
        mul=n1*n2
        return mul

    def divide_numbers(self,n1,n2):
        result=n1/n2
        return result


